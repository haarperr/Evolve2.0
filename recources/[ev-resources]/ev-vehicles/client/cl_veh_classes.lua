function GetVehicleClass(veh)
    for i = 1, #vehicles do
        if vehicles[i].model == firstToUpper(veh) then
            return vehicles[i].class
        end
    end
end

function GetVehicleName(veh)
    for i = 1, #vehicles do
        if tonumber(vehicles[i].hash) == tonumber(veh) then
            return vehicles[i].model
        end
    end
end

function GetVehicleTable()
    return vehicles
end

function SetVehicleMods(plate)
    local vehicle = plate
    PerformanceUpgradeVehicle(vehicle)
end

local performanceModIndices = {11,12,13,15,16}
function PerformanceUpgradeVehicle(vehicle, customWheels)
    customWheels = customWheels or false
    local max
    if DoesEntityExist(vehicle) and IsEntityAVehicle(vehicle) then
        for _, modType in ipairs(performanceModIndices) do
            max = GetNumVehicleMods(vehicle, modType) - 1
            SetVehicleMod(vehicle, modType, max, customWheels)
        end
        ToggleVehicleMod(vehicle, 18, true) -- Turbo
        SetVehicleFixed(vehicle)
        SetVehicleDirtLevel(vehicle, 0)
    end
end

function firstToUpper(str)
    if str ~= nil then
        str = string.lower(str)
        str = string.gsub(str, "%s+", "")
        return str
    else
        return false
    end
end